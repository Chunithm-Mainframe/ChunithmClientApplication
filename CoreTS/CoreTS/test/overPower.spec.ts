import { getOverPower } from "../src/Rating";
import * as util from "../src/Utility";
import { ComboStatus, Rank, getBorderScore } from "../src/Utility";

function checkCommon(expectedOverPower: number, baseRating: number, score: number, comboStatus: ComboStatus, message: string): void {
    var actualOverPower = getOverPower(baseRating, score, comboStatus);
    var _message = message + "\n"
        + "baseRating: " + baseRating + ", score: " + score + ", comboStatus: " + comboStatus + "\n"
        + "expected: " + expectedOverPower + "\n"
        + "actual: " + actualOverPower;

    var error = 1e-13;
    expect(Math.abs(actualOverPower - expectedOverPower) <= error).toBeTruthy();

    // 本当は↓で通ってほしい
    //expect(expectedOverPower).toBe(actualOverPower, _message);
}

function checkBorder(expectedOverPower: number, baseRating: number, rank: Rank, comboStatus: ComboStatus): void {
    var message = "checkBorder" + "\n"
        + "rank: " + util.toRankText(rank);
    checkCommon(expectedOverPower, baseRating, util.getBorderScore(rank), comboStatus, message);
}

function checkMedian(expectedOverPower: number, baseRating: number, rank1: Rank, rank2: Rank, comboStatus: ComboStatus): void {
    var message = "checkMedian" + "\n"
        + "rank1: " + util.toRankText(rank1) + ", rank2: " + util.toRankText(rank2);
    var score = (util.getBorderScore(rank1) + util.getBorderScore(rank2)) / 2;
    checkCommon(expectedOverPower, baseRating, score, comboStatus, message);
}

function checkJustBeforeBorder(expectedOverPower: number, baseRating: number, rank: Rank, comboStatus: ComboStatus): void {
    var message = "checkJustBeforeBorder" + "\n"
        + "rank: " + util.toRankText(rank);
    checkCommon(expectedOverPower, baseRating, util.getBorderScore(rank) - 1, comboStatus, message);
}

function checkJustBeforeMedian(expectedOverPower: number, baseRating: number, rank1: Rank, rank2: Rank, comboStatus: ComboStatus): void {
    var message = "checkJustBeforeMedian" + "\n"
        + "rank1: " + util.toRankText(rank1) + ", rank2: " + util.toRankText(rank2);
    var score = (util.getBorderScore(rank1) + util.getBorderScore(rank2)) / 2 - 1;
    checkCommon(expectedOverPower, baseRating, score, comboStatus, message);
}

function checkOutOfRange(expectedOverPower: number, baseRating: number, score: number, comboStatus: ComboStatus): void {
    var message = "checkOutOfRange";
    checkCommon(expectedOverPower, baseRating, score, comboStatus, message);
}

